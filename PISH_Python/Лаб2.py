# Задание 3
# Логическая функция: сумма цифр равна произведению цифр
# ===========================================

def is_sum_equal_product(n):
    n = abs(n)
    digits = [int(d) for d in str(n)]
    s = sum(digits)
    p = 1
    for d in digits:
        p *= d
    return s == p


print("\nЗадание 3: Проверка, равна ли сумма цифр произведению")
n = int(input("Введите целое число: "))
if is_sum_equal_product(n):
    print("Сумма цифр равна произведению цифр")
else:
    print("Сумма цифр не равна произведению цифр")

