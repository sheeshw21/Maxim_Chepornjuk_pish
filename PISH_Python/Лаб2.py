# Задание 2
# Функция вычисления наибольшего общего делителя (НОД)
# ===========================================

def nod(a, b):
    while b != 0:
        a, b = b, a % b
    return abs(a)


print("\nЗадание 2: Нахождение НОД двух чисел")
a = int(input("Введите первое число a: "))
b = int(input("Введите второе число b: "))
print(f"НОД({a}, {b}) = {nod(a, b)}")

