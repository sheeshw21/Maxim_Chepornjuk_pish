# Задание 4
# Функция для вычисления суммы цифр в числе
# ===========================================

def sum_of_digits(n):
    n = abs(n)
    return sum(int(d) for d in str(n))


print("\nЗадание 4: Нахождение суммы цифр числа")
n = int(input("Введите целое число: "))
print(f"Сумма цифр числа {n} = {sum_of_digits(n)}")
