# Задание 1
# Описать функцию вычисления:
# f(x) = x^2, если -2 <= x < 2
# f(x) = x^2 + 4x + 5, если x >= 2
# f(x) = 4, если x < -2
# ===========================================

def f(x):
    if -2 <= x < 2:
        return x ** 2
    elif x >= 2:
        return x ** 2 + 4 * x + 5
    else:
        return 4


print("Задание 1: Вычисление значения функции f(x)")
x = float(input("Введите значение x: "))
print(f"Результат: f({x}) = {f(x)}")

