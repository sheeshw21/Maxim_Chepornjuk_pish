# У героя есть:
#   - 1 меч
#   - 2 ножа
#   - 1 баночка яда
#
# Чтобы победить дракона, нужно:
#   - либо 2 меча
#   - либо (баночка яда и 2 ножа)
# ===========================================

# Количество предметов у героя
swords = 1     # мечи
knives = 2     # ножи
poison = 1     # баночки яда

# Условие победы над драконом
can_win = (swords >= 2) or (poison >= 1 and knives >= 2)

# Проверка условия
if can_win:
    print("Герой может победить дракона!")
else:
    print("Герой не готов к битве с драконом.")
